package date0124;

public class P1_ {

	public static void main(String[] args) {
		int a=100,b=50;
		char oper='+';
		switch(oper) {
		case '+':
			System.out.println("a+b= "+(a+b));
			break;
		case '-':
			System.out.println("a-b= "+(a-b));
			break;
		case '*':
			System.out.println("a*b= "+(a*b));
			break;
		case '/':
			System.out.println("a/b= "+(a/b));
			break;
		default:
			System.out.println("a@b= @@@@");
		}

	}

}
